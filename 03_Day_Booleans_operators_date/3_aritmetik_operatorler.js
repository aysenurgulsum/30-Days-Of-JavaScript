// arttırma operatörü

let count = 0

console.log(++count)  // 1
console.log(count)   // 1

count = 0

console.log(count++) //0
console.log(count)  //1


//azaltma operatörü

count = 0

console.log(--count) // -1
console.log(count)  // -1

count = 0

console.log(count--) // 0
console.log(count)  // -1