//Number
x = 3
y = 4.2

console.log(typeof x)  // output: number
console.log(typeof y)  // output: number


//String
a = "Ayşenur"

console.log(typeof a)  // output: string


//Boolean
b = true

console.log(typeof b)  // output: boolean


//Undefined
let isim

console.log(typeof isim)  // output: undefined, çünkü değişkene henüz değer ataması yapılmadı


//Null
let bos = null
console.log(typeof bos)  // output: object
